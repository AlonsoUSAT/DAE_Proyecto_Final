    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
         if (txtID.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese un ID para buscar.", "Validaci贸n", JOptionPane.WARNING_MESSAGE);
            return;
        }
        try {
            int id = Integer.parseInt(txtID.getText());
            clsUnidad unidad = objUnidad.buscarPorId(id);
            if (unidad != null) {
                unidadSeleccionadaActual = unidad; 
                cargarDatos(unidad);
                gestionarEstadoControles("seleccionado");
            } else {
                JOptionPane.showMessageDialog(this, "No se encontr贸 registro con el ID: " + id, "B煤squeda", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (NumberFormatException e) {
             JOptionPane.showMessageDialog(this, "El ID debe ser un n煤mero.", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
             JOptionPane.showMessageDialog(this, "Error al buscar: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
         if (btnNuevo.getText().equals("Nuevo")) {
            gestionarEstadoControles("nuevo");
            limpiarCampos();
            try {
                txtID.setText(String.valueOf(objUnidad.generarNuevoId()));
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error al generar ID: " + e.getMessage());
            }
            txtNombre.requestFocus();
        } else { // Guardar
            if (txtNombre.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "El nombre es obligatorio.", "Validaci贸n", JOptionPane.WARNING_MESSAGE);
                return;
            }
            try {
                clsUnidad unidad = new clsUnidad();
                unidad.setId(Integer.parseInt(txtID.getText()));
                unidad.setNombre(txtNombre.getText());
                unidad.setEstado(chkEstado.isSelected());
                objUnidad.registrar(unidad);
                JOptionPane.showMessageDialog(this, "Unidad registrada correctamente.", "xito", JOptionPane.INFORMATION_MESSAGE);
                actualizarTabla();
                gestionarEstadoControles("inicio");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error al registrar: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnNuevoActionPerformed


    private void lstMediosDePagoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lstMediosDePagoMouseClicked
        // TODO add your handling code here:
        try {
            String itemSeleccionado = lstMediosDePago.getSelectedValue();
            if (itemSeleccionado != null && !itemSeleccionado.isEmpty()) {
                // Separar el ID del texto (formato "1 - Efectivo")
                String[] partes = itemSeleccionado.split(" - ");
                if (partes.length > 0) {
                    txtId.setText(partes[0].trim());
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, 
                "Error al seleccionar medio de pago: " + e.getMessage(), 
                "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_lstMediosDePagoMouseClicked

    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
        try {
            if (btnNuevo.getText().equalsIgnoreCase("Nuevo")) {
                btnNuevo.setText("Guardar");

                Integer nuevoCodigo = objMedioPago.generarCodigoMedioPago();

                txtId.setText(nuevoCodigo.toString());
                txtNombre.setText("");
                txtNombre.requestFocus();

            } else {
                Integer id = Integer.parseInt(txtId.getText().trim());
                String nombre = txtNombre.getText().trim();

                objMedioPago.insertarMedioPago(id, nombre);

                JOptionPane.showMessageDialog(this, 
                    "Medio de pago registrado correctamente.", 
                    "Éxito", JOptionPane.INFORMATION_MESSAGE);

                listarMediosPago();

                btnNuevo.setText("Nuevo");
                txtId.setText("");
                txtNombre.setText("");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, 
                "Error: " + e.getMessage(), 
                "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnNuevoActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        try {
            if (btnModificar.getText().equalsIgnoreCase("Modificar")) {
                btnModificar.setText("Guardar");

                if (txtId.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(this, 
                        "Seleccione un medio de pago de la lista antes de modificar.", 
                        "Advertencia", JOptionPane.WARNING_MESSAGE);
                    btnModificar.setText("Modificar");
                    return;
                }

                txtId.setEditable(false);
                lstMediosDePago.setEnabled(false);
                txtNombre.setEnabled(true);
                txtNombre.requestFocus();

            } else {
                Integer id = Integer.parseInt(txtId.getText().trim());
                String nombre = txtNombre.getText().trim();

                if (nombre.isEmpty()) {
                    JOptionPane.showMessageDialog(this, 
                        "Debe ingresar un nombre para el medio de pago.", 
                        "Advertencia", JOptionPane.WARNING_MESSAGE);
                    return;
                }

                objMedioPago.modificarMedioPago(id, nombre);

                JOptionPane.showMessageDialog(this, 
                    "Medio de pago actualizado correctamente.", 
                    "Éxito", JOptionPane.INFORMATION_MESSAGE);

                listarMediosPago();
                txtId.setText("");
                txtNombre.setText("");

                txtNombre.setEnabled(false);
                txtId.setEditable(true);
                lstMediosDePago.setEnabled(true);
                btnModificar.setText("Modificar");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, 
                "Error al modificar: " + e.getMessage(), 
                "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        try {
            int id = 0;
            if (!txtId.getText().trim().isEmpty()) {
                id = Integer.parseInt(txtId.getText().trim());
            } else {
                String nombre = txtNombre.getText().trim();
                if (nombre.isEmpty()) {
                    JOptionPane.showMessageDialog(this,
                        "Seleccione un medio de pago o ingrese el nombre para eliminar.",
                        "Atención", JOptionPane.WARNING_MESSAGE);
                    return;
                }
                id = objMedioPago.obtenerIdPorNombre(nombre);
                if (id == 0) {
                    JOptionPane.showMessageDialog(this,
                        "No se encontró ningún medio de pago con ese nombre.",
                        "No encontrado", JOptionPane.WARNING_MESSAGE);
                    return;
                }
            }

            int confirm = JOptionPane.showConfirmDialog(this,
                "¿Está seguro que desea eliminar el medio de pago con ID: " + id + " ?",
                "Confirmar eliminación",
                JOptionPane.YES_NO_OPTION);

            if (confirm == JOptionPane.YES_OPTION) {
                objMedioPago.eliminarMedioPago(id);

                JOptionPane.showMessageDialog(this,
                    "Medio de pago eliminado correctamente.",
                    "Éxito", javax.swing.JOptionPane.INFORMATION_MESSAGE);

                listarMediosPago();
                txtId.setText("");
                txtNombre.setText("");
                btnModificar.setText("Modificar");
                btnNuevo.setText("Nuevo");
            }

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this,
                "ID inválido: " + ex.getMessage(),
                "Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                "Error al eliminar medio de pago: " + e.getMessage(),
                "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed



public class clsLaboratorio {
    private int idLaboratorio;
    private String nombreLaboratorio;
    private String direccion;
    private String telefono;
    private boolean estado;

    public boolean isEstado() {
        return estado;
    }

    public void setEstado(boolean estado) {
        this.estado = estado;
    }

    public int getIdLaboratorio() {
        return idLaboratorio;
    }

    public void setIdLaboratorio(int idLaboratorio) {
        this.idLaboratorio = idLaboratorio;
    }

    public String getNombreLaboratorio() {
        return nombreLaboratorio;
    }

    public void setNombreLaboratorio(String nombreLaboratorio) {
        this.nombreLaboratorio = nombreLaboratorio;
    }

    public String getDireccion() {
        return direccion;
    }

    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    public String getTelefono() {
        return telefono;
    }

    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }
    
    @Override
public String toString() {
    return this.getNombreLaboratorio(); // Esto har√° que el ComboBox muestre el nombre
}
@Override
public int hashCode() {
    int hash = 5;
    hash = 97 * hash + this.idLaboratorio;
    return hash;
}

@Override
public boolean equals(Object obj) {
    if (this == obj) {
        return true;
    }
    if (obj == null) {
        return false;
    }
    if (getClass() != obj.getClass()) {
        return false;
    }
    final clsLaboratorio other = (clsLaboratorio) obj;
    return this.idLaboratorio == other.idLaboratorio;
}
}

    private float calcularTotalVenta(){
        float total = 0;
        DefaultTableModel modelo = (DefaultTableModel) tblDetalleVenta.getModel();
        for(int i=0; i < modelo.getRowCount(); i++){
            total += Float.parseFloat(modelo.getValueAt(i, 4).toString());
        }
        return total;
    }


    // 2. M茅todo para listar presentaciones (corregido para usar DTO)
    private void listarPresentaciones() {
    DefaultTableModel modelo = (DefaultTableModel) tblPresentacion.getModel();
    modelo.setRowCount(0); // Limpiar la tabla

    try {
        ArrayList<PresentacionDAO> lista = objPresentacion.listarPresentaciones();
        
        for (PresentacionDAO dto : lista) {
            // AQU EST LA CORRECCIN
            modelo.addRow(new Object[]{
                dto.getId(),
                dto.getNombreTipoPresentacion(), // 锔 CORREGIDO
                dto.getCantidad(),
                dto.getNombreUnidad()             // 锔 CORREGIDO
            });
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al cargar presentaciones: " + e.getMessage());
    }
}

    // 3. M茅todo para listar tipos de presentaci贸n (corregido para usar DTO)
    private void listarTiposPresentacion() {
        try {
            cmbTipoPresentacion.removeAllItems();
            ArrayList<TipoPresentacionDAO> lista = objTipoPresentacion.listarTiposPresentacion();
            
            for (TipoPresentacionDAO dto : lista) {
                // Se a帽aden los objetos completos al ComboBox
                cmbTipoPresentacion.addItem(dto);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar tipos de presentaci贸n: " + e.getMessage());
        }
    }

    // 4. M茅todo para listar unidades (COMPLETAMENTE CORREGIDO)
private void listarUnidades() {
    try {
        cmbUnidad.removeAllItems();
        // La siguiente l铆nea debe usar UnidadDAO
        ArrayList<UnidadDAO> lista = objUnidad.listarUnidades();
        
        // El bucle tambi茅n debe usar UnidadDAO
        for (UnidadDAO dao : lista) {
            // Se a帽aden los objetos DAO (que act煤an como DTO) al ComboBox
            cmbUnidad.addItem(dao);
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al cargar unidades: " + e.getMessage());
    }
}
    

    public void listarMediosPago() {
        modeloLista.clear();
        try {
            ResultSet rs = objMedioPago.listarMediosPago();
            while (rs.next()) {
                String item = rs.getInt("idmediopago") + " - " + rs.getString("nombre");
                modeloLista.addElement(item);
            }
            lstMediosDePago.setModel(modeloLista);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, 
                "Error al listar medios de pago: " + e.getMessage(), 
                "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    

